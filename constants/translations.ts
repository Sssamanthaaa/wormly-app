export type AppLanguage = keyof typeof translations;
export type TranslationKey = keyof (typeof translations)["English"];

export const languageLocaleMap: Record<AppLanguage, string> = {
  English: "en-US",
  Spanish: "es-ES",
  French: "fr-FR",
};

export function getLocale(language: AppLanguage) {
  return languageLocaleMap[language];
}

export function translate(
  language: AppLanguage,
  key: TranslationKey,
  params: Record<string, string | number> = {}
) {
  let value = translations[language][key] ?? translations.English[key];

  for (const [param, replacement] of Object.entries(params)) {
    value = value.replaceAll(`{${param}}`, String(replacement));
  }

  return value;
}
